number = int(input("Введіть трицифрове число: "))

# виділяємо першу цифру за допомогою цілочисельного ділення на 100
first = number // 100

# виділяємо останню цифру за допомогою операції залишку від ділення на 10
last = number % 10

# перевіряємо, чи співпадають перша і остання цифри 
if first == last:
    print("Число є паліндромом")
else:
    print("Число не є паліндромом")
